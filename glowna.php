<h1 class="ng">O NAS</h1>
<p>Firma MEBLECH specjalizuje się w produkcji mebli kuchennych i szaf z drzwiami przesuwnymi  (zarówno klasycznych jak i nowoczesnych), wyposażenia biur i gabinetów.<br>
W ofercie znajdują się fronty MDF PCV, MDF malowany, drewno lite, forniry egzotyczne oraz szkło i aluminium.<br>
Zapewniamy doradztwo, projekt, montaż i transport.<br>
Ponad 20 letnie doświadczenie w branży drzewnej pozwala sprostać wykonaniu nawet najbardziej nietypowych projektów.</p>
          